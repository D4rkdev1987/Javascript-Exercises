//Modern way to use string interpolation

const lyrics = 'Never gonna give up';
console.log("im " + lyrics);
console.log(`im ` + lyrics); //backticks -->
console.log(`im  ${lyrics}`); //-->to use javascript -"im never gonna give up"
console.log(`im  ${2 + 2}`); //-->to use javascript - "im 4"
//doesn't have to be just a variable it runs it and treats it as JS- you can put anything in it

